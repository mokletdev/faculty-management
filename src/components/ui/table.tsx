function SortableTableHead({
  className,
  children,
  sorted = false,
  direction = "none",
  onSort,
  ...props
}: React.ComponentProps<"th"> & {
  sorted?: boolean;
  direction?: "none" | "asc" | "desc";
  onSort?: () => void;
}) {
  return (
    <th
      data-slot="table-head"
      className={cn(
        "group h-12 cursor-pointer px-4 text-left align-middle font-semibold whitespace-nowrap text-neutral-700 select-none",
        className,
      )}
      onClick={onSort}
      {...props}
    >
      <div className="flex items-center gap-2">
        {children}
        <span className="inline-flex">
          {direction === "none" && (
            <svg
              xmlns="http://www.w3.org/2000/svg"
              width="16"
              height="16"
              viewBox="0 0 24 24"
              fill="none"
              stroke="currentColor"
              strokeWidth="2"
              strokeLinecap="round"
              strokeLinejoin="round"
              className="opacity-0 group-hover:opacity-30"
            >
              <path d="M7 3L17 3M7 9L13 9M7 15L17 15M7 21L13 21" />
            </svg>
          )}
          {direction === "asc" && (
            <svg
              xmlns="http://www.w3.org/2000/svg"
              width="16"
              height="16"
              viewBox="0 0 24 24"
              fill="none"
              stroke="currentColor"
              strokeWidth="2"
              strokeLinecap="round"
              strokeLinejoin="round"
            >
              <path d="m5 15 7-7 7 7" />
            </svg>
          )}
          {direction === "desc" && (
            <svg
              xmlns="http://www.w3.org/2000/svg"
              width="16"
              height="16"
              viewBox="0 0 24 24"
              fill="none"
              stroke="currentColor"
              strokeWidth="2"
              strokeLinecap="round"
              strokeLinejoin="round"
            >
              <path d="m19 9-7 7-7-7" />
            </svg>
          )}
        </span>
      </div>
    </th>
  );
}

